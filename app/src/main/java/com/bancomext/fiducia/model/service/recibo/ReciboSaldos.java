package com.bancomext.fiducia.model.service.recibo;

public class ReciboSaldos {

    int saldoContratoNumero;
    long saldoFecha;
    int atencionContratoNombre;
    String saldoCtoInvNombre;
    String saldoCtoInvMoneda;
    int saldoCtoInvSaldo;
    String saldoFechaReg;

    public int getSaldoContratoNumero() {
        return saldoContratoNumero;
    }

    public void setSaldoContratoNumero(int saldoContratoNumero) {
        this.saldoContratoNumero = saldoContratoNumero;
    }

    public long getSaldoFecha() {
        return saldoFecha;
    }

    public void setSaldoFecha(long saldoFecha) {
        this.saldoFecha = saldoFecha;
    }

    public int getAtencionContratoNumero() {
        return atencionContratoNombre;
    }

    public void setAtencionContratoNumero(int atencionContratoNumero) {
        this.atencionContratoNombre = atencionContratoNumero;
    }

    public String getSaldoCtoInvNombre() {
        return saldoCtoInvNombre;
    }

    public void setSaldoCtoInvNombre(String saldoCtoInvNombre) {
        this.saldoCtoInvNombre = saldoCtoInvNombre;
    }

    public String getSaldoCtoInvMoneda() {
        return saldoCtoInvMoneda;
    }

    public void setSaldoCtoInvMoneda(String saldoCtoInvMoneda) {
        this.saldoCtoInvMoneda = saldoCtoInvMoneda;
    }

    public int getSaldoCtoInvSaldo() {
        return saldoCtoInvSaldo;
    }

    public void setSaldoCtoInvSaldo(int saldoCtoInvSaldo) {
        this.saldoCtoInvSaldo = saldoCtoInvSaldo;
    }

    public String getSaldoFechaReg() {
        return saldoFechaReg;
    }

    public void setSaldoFechaReg(String saldoFechaReg) {
        this.saldoFechaReg = saldoFechaReg;
    }
}
